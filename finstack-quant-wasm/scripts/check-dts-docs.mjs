import { readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const dts = readFileSync(join(root, 'index.d.ts'), 'utf8');
const readme = readFileSync(join(root, 'README.md'), 'utf8');

const ownedClasses = [
  'Performance',
  'CreditFactorModel',
  'CreditCalibrator',
  'LevelsAtDate',
  'PeriodDecomposition',
  'FactorCovarianceForecast',
  'Market',
  'Portfolio',
];

const failures = [];

for (const className of ownedClasses) {
  if (!dts.includes(`export interface ${className} extends WasmOwned {}`)) {
    failures.push(`${className} must merge the WasmOwned contract in index.d.ts`);
  }
}

if (!dts.includes('export interface WasmOwned')) {
  failures.push('index.d.ts must declare the shared WasmOwned contract');
}
if (!dts.includes('free(): void;')) {
  failures.push('WasmOwned must strongly expose free()');
}
if (dts.includes('[Symbol.dispose](): void;')) {
  failures.push('base declarations must not require esnext.disposable');
}
if (!dts.includes('installs `[Symbol.dispose]` as an alias of `free`')) {
  failures.push('index.d.ts must document the conditional runtime Symbol.dispose alias');
}
if (!dts.includes('WASM ownership: every wasm-bindgen class')) {
  failures.push('index.d.ts must include the package-level WASM ownership note');
}

if (!readme.includes('## WASM Object Disposal')) {
  failures.push('README.md must include the WASM Object Disposal section');
}
if (
  !readme.includes('Every class generated by `wasm-bindgen`') ||
  !readme.includes('`Symbol.dispose`') ||
  !readme.includes('intentionally omit the')
) {
  failures.push('README disposal docs must describe free() and compatible Symbol.dispose behavior');
}

if (failures.length > 0) {
  console.error(failures.map((failure) => `- ${failure}`).join('\n'));
  process.exit(1);
}
